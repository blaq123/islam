const numTwo = 2;

//FOR LOOPs
for (let x = 1; x <= 12; x++) {
    console.log(`${numTwo} x ${x} = ${x * numTwo}`);
}

for (let x = 5; x >= 1; x--){
    console.log(x);
}

//WHILE LOOP
let i = 5;
while (i <= 10) {
    console.log(i);
    i++;
}

//DO WHILE LOOP
let z = 10;
do {
    console.log(z);
    z--;
    
} while (z >= 1);
